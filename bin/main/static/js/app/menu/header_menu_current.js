function menuCurrent (menu) {
	if (menu == "home" ) {
		$("#home_menu").css("color", "rgb(252,220,10)");	
	} else if (menu == "news" ) {
		$("#news_menu").css("color", "rgb(252,220,10)");	
	} else if (menu == "ranking" ) {
		$("#ranking_menu").css("color", "rgb(252,220,10)");	
	} else if (menu == "result" ) {
		$("#result_menu").css("color", "rgb(252,220,10)");	
	} else if (menu == "cup" ) {
		$("#cup_menu").css("color", "rgb(252,220,10)");	
	}
}